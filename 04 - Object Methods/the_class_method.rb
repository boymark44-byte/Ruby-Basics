# Class - A blueprint for an object. (schematic or template)
# Object - An "instance" of the class.


# Returns String class.
puts "Hello".class  # Returns the class from which this object is made from or instance.
puts "Ruby is fun.".class
puts "12345".class
puts "".class


# Returns Integer class.
puts 5.class
puts -23.class
puts 100.class
puts 999_999_999.class


# Returns Float class
puts 3.14.class
puts 0.23.class
puts -10.49.class
