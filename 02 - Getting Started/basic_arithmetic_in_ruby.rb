# Addition
puts 1 + 4
puts -6 + 13


# Subtraction
puts 10 - 6


# Multiplication
puts 3 * 4
puts 3 * 4 * 5


# PEMDAS - Order of operators or precedence
puts 2 + 3 * 5
puts (2 + 3) * 5 # If you put parenthesis, it will be followed first.


# Division - Floor Division means always returning the quotient in integer.
puts 10 / 5
puts 12 / 5 # Chopping off the decimal compenents, not rounding off.


# Division
puts 12.0 / 5 # The same thing in algebra, if we divide a decimal value to an integer, it will return a decimal result.
puts 12 / 5.0
puts 12.0 / 5.0


puts 0.5 / 2


# Modulo - which will gives us the remainder of the quotient.
puts 5 % 2
puts 7 % 2
puts 6 % 2
